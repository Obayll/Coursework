T = 1/4000;
tt = -T:(2*T)/100:T;
A1 = 19;
A2 = 1.2*A1;
M = 6;
D = 1;
t1 = (37.2/M)*T;
t2 = -(41.3/D)*T;
x1 = A1*cos(2*pi*(4000)*(tt-t1));
x2 = A2*cos(2*pi*(4000)*(tt-t2));
x3 = x1 + x2;
subplot(311);
plot(tt,x1);
title('Jordan Ditzler - x1');
subplot(312);
plot(tt,x2);
title('x2');
subplot(313);
plot(tt,x3);
title('x3');